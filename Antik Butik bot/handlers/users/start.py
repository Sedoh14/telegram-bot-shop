@dp.message_handler(CommandStart())
async def bot_start(message: types.Message):
    if str(message.from_user.id) == str(admins):
        await message.answer(f"–ü—Ä–∏–≤–µ—Ç, {message.from_user.full_name}!\n"
                             f" –ß—Ç–æ–±—ã –¥–æ–±–∞–≤–∏—Ç—å –Ω–æ–≤—ã–π —Ç–æ–≤–∞—Ä –Ω–∞–∂–º–∏—Ç–µ /add_item\n"
                             f" –ß—Ç–æ–±—ã —É–¥–∞–ª–∏—Ç—å —Ç–æ–≤–∞—Ä –Ω–∞–∂–º–∏—Ç–µ /remove_item\n"
                             f' –ß—Ç–æ–±—ã –¥–æ–±–∞–≤–∏—Ç—å –Ω–æ–≤–æ–≥–æ –ø—Ä–µ–¥—Å—Ç–∞–≤–∏—Ç–µ–ª—è —Ç–µ—Ö. –ø–æ–¥–¥–µ—Ä–∂–∫–∏ –Ω–∞–∂–º–∏—Ç–µ /add_supporter\n'
                             f' –ß—Ç–æ–±—ã —É–¥–∞–ª–∏—Ç—å id –ø—Ä–µ–¥—Å—Ç–∞–≤–∏—Ç–µ–ª—è —Ç–µ—Ö. –ø–æ–¥–¥–µ—Ä–∂–∫–∏ –Ω–∞–∂–º–∏—Ç–µ /remove_supporter',
                             reply_markup=start_keyboard)
    else:
        await message.answer(f"–ü—Ä–∏–≤–µ—Ç, {message.from_user.full_name}!"
                             f" –Ø –±–æ—Ç –ê–Ω—Ç–∏–∫—ä-–ë—É—Ç–∏–∫—ä. –ß—Ç–æ –≤–∞—Å –∏–Ω—Ç–µ—Ä–µ—Å—É–µ—Ç?", reply_markup=start_keyboard)


